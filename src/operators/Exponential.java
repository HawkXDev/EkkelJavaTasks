package operators;

public class Exponential {
    public static void main(String[] args) {
        float maxFloatExponential = Float.MAX_VALUE;
        System.out.printf("Max Float in exponential form = %e%n", maxFloatExponential);
        double maxDoubleExponential = Double.MAX_VALUE;
        System.out.printf("Max Double in exponential form = %e%n", maxDoubleExponential);
        float minFloatExponential = Float.MIN_VALUE;
        System.out.printf("Min Float in exponential form = %e%n", minFloatExponential);
        double minDoubleExponential = Double.MIN_VALUE;
        System.out.printf("Min Double in exponential form = %e%n", minDoubleExponential);
    }
}
